package com.xworkz.hospitalapp.hospital.hospital;

public class Hospital {

        private   int hospitalId;

        public int getHospitalId() {
            return hospitalId;
        }

        public void setHospitalId(int hospitalId) {
            this.hospitalId = hospitalId;
        }

        private int hospitalName;

        public int getHospitalName() {
            return hospitalName;
        }

        public void setHospitalName(int hospitalName) {
            this.hospitalName = hospitalName;
        }

        private   String hospitalType;

        public String getHospitalType() {
            return hospitalType;
        }

        public void setHospitalType(String hospitalType) {
            this.hospitalType = hospitalType;
        }


        private  String hospitalLocation;

        public String getHospitalLocation() {
            return hospitalLocation;
        }

        public void setHospitalLocation(String hospitalLocation) {
            this.hospitalLocation = hospitalLocation;
        }

        private String numberOfDoctors;

        public String getNumberOfDoctors() {
            return numberOfDoctors;
        }

        public void setNumberOfDoctors(String numberOfDoctors) {
            this.numberOfDoctors = numberOfDoctors;
        }
    }

