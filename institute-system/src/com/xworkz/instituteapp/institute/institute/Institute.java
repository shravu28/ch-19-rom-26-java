package com.xworkz.instituteapp.institute.institute;

public class Institute {

        private   int instituteId;

        public int getInstituteId() {
            return instituteId;
        }

        public void setInstituteId(int instituteId) {
            this.instituteId = instituteId;
        }

        private int instituteName;

        public int getInstituteName() {
            return instituteName;
        }

        public void setInstituteName(int instituteName) {
            this.instituteName = instituteName;
        }

        private   String instituteType;

        public String getInstituteType() {
            return instituteType;
        }

        public void setInstituteType(String instituteType) {
            this.instituteType = instituteType;
        }


        private  String instituteLocation;

        public String getInstituteLocation() {
            return instituteLocation;
        }

        public void setInstituteLocation(String instituteLocation) {
            this.instituteLocation = instituteLocation;
        }

        private String numberOfInstitute;

        public String getNumberOfInstitute() {
            return numberOfInstitute;
        }

        public void setNumberOfInstitute(String numberOfInstitute) {
            this.numberOfInstitute = numberOfInstitute;
        }
    }



